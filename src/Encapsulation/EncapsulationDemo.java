package Encapsulation;

public class EncapsulationDemo {
	
	private String name;
	private int rollno;
	private int age;
	public String getName() {
		return name;
	}
	public void setName(String n) {
		name = n;
	}
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
